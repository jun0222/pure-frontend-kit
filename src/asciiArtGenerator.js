// アスキーアートを生成する関数
export function generateAsciiArt(text) {
  let asciiArt = ["", "", "", "", ""];

  for (const char of text) {
    if (asciiArtMap[char]) {
      const lines = asciiArtMap[char];
      for (let i = 0; i < lines.length; i++) {
        asciiArt[i] += lines[i] + "  "; // 文字間のスペースを追加
      }
    }
  }

  return asciiArt.join("\n");
}
